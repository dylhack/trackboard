export default class extends Controller {
  static values = {
    default: { type: String, default: "default" },
    minLength: { type: Number, default: 0 }
  }

  static targets = [ 'output' ]

  connect() {
    this.#update(this.defaultValue)
  }

  change(event) {
    this.#update(event.target.value)
  }

  #update(value) {
    const icon = this.#getIcon(value)
    this.outputTargets.forEach((element) => {
      element.innerHTML = icon
    })
  }

  #getIcon(value) {
    if (value.length <= this.minLengthValue) {
      return this.#defaultIcon
    }

    const target = value.trim().toLowerCase()
    const iconSet = Object.entries(ICON_SET)
    for (const [iconName, icon] of iconSet) {
      if (target.includes(iconName)) {
        return icon
      }
    }

    return this.#defaultIcon
  }

  get #defaultIcon() {
    return ICON_SET.default
  }
}
